  void displayBillDetails()
    {
         Connection con =getConnection();//accessing database connection from DB_Connection class
        try {
              stmt=con.createStatement();//create Statement
             ResultSet rs=stmt.executeQuery("select * from Bill_details");
           while(rs.next())
            {
                //data will be added until finish
               
                String billno=String.valueOf(rs.getInt("Bill_No"));
                String billdate=String.valueOf(rs.getDate("Bill_Date"));
                String billtype=rs.getString("Bill_Type");
                String opdtype=String.valueOf(rs.getString("OPD_Type"));
                String opdno=String.valueOf(rs.getInt("OPD_No"));
                String patientname=rs.getString("Patient_Name");
                String pregno=String.valueOf(rs.getInt("PRegNo"));  
                String service=rs.getString("Services");
                String samount=rs.getString("Service_Amount");
                String discount=rs.getString("Discount");
                 String total=rs.getString("Total_Amount");
                  String paymode=rs.getString("PayMode");
                
                //String array for store dara into Patient_tbl
                String tbData[]={ billno,billdate,billtype,opdtype,opdno,patientname,pregno,service,samount,discount,total,paymode};
                DefaultTableModel model=(DefaultTableModel)bill_tbl.getModel();  
                //add String array data into Patient_tbl
                model.addRow(tbData);
            }
           con.close();//connection close
        } catch (Exception e) {
           e.printStackTrace();
       }
    }
    
