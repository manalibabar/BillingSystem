    public void setDataToCards(){
        try {
               Connection con =getConnection();//accessing database connection from DB_Connection class
              stmt=con.createStatement();
              ResultSet rs=stmt.executeQuery("Select * from Patient");
              rs.last();
              TotalPatient.setText(Integer.toString(rs.getRow()));
              
              
              rs=stmt.executeQuery("Select * from Appointment");
              rs.last();
              Total_appt.setText(Integer.toString(rs.getRow()));
              
              rs=stmt.executeQuery("Select * from Bill_details");
              rs.last();
              Total_bills.setText(Integer.toString(rs.getRow()));
                   
        } catch (Exception e) {
            e.printStackTrace();
        }
     
    }
    
    
    
    
    
    
